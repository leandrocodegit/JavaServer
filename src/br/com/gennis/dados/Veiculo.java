package br.com.gennis.dados;

/**
 *
 * @author LEANDRO
 */
public class Veiculo {
    
    private String placa;
    private String idCadastro;
    private String dispositivo;
    private String vaga;
    private boolean bloqueio;
    private String cor;
    private String marca;
    private boolean motocilcleta;
    private String unidade;

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getIdCadastro() {
        return idCadastro;
    }

    public void setIdCadastro(String idCadastro) {
        this.idCadastro = idCadastro;
    }

    public String getDispositivo() {
        return dispositivo;
    }

    public void setDispositivo(String dispositivo) {
        this.dispositivo = dispositivo;
    }

    public boolean isBloqueio() {
        return bloqueio;
    }

    public void setBloqueio(boolean bloqueio) {
        this.bloqueio = bloqueio;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public boolean isMotocilcleta() {
        return motocilcleta;
    }

    public void setMotocilcleta(boolean motocilcleta) {
        this.motocilcleta = motocilcleta;
    }

    public String getUnidade() {
        return unidade;
    }

    public void setUnidade(String unidade) {
        this.unidade = unidade;
    }

    public String getVaga() {
        return vaga;
    }

    public void setVaga(String vaga) {
        this.vaga = vaga;
    }

    
    
    
   
    
    
    
    
}
